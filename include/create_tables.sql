CREATE TABLE IF NOT EXISTS RAW_TABLE (
	"ID" NUMBER(38,0) PRIMARY KEY,
	"PASSENGERCODE" VARCHAR(16777216),
	"FIRSTNAME" VARCHAR(16777216),
	"LASTNAME" VARCHAR(16777216),
	"GENDER" VARCHAR(16777216),
	"AGE" NUMBER(38,0),
	"NATIONALITY" VARCHAR(16777216),
	"AIRPORTNAME" VARCHAR(16777216),
	"AIRPORTCOUNTRYCODE" VARCHAR(16777216),
	"COUNTRYNAME" VARCHAR(16777216),
	"AIRPORTCONTINENT" VARCHAR(16777216),
	"CONTINENTS" VARCHAR(16777216),
	"DEPARTUREDATE" DATE,
	"ARRIVALAIRPORT" VARCHAR(16777216),
	"PILOTNAME" VARCHAR(16777216),
	"FLIGHTSTATUS" VARCHAR(16777216),
	"TICKETTYPE" VARCHAR(16777216),
	"PASSENGERSTATUS" VARCHAR(16777216)
);

CREATE TABLE IF NOT EXISTS PASSENGERS (
	"PASSENGERID" INT AUTOINCREMENT start 1 increment 1 PRIMARY KEY,
	"PASSENGERCODE" VARCHAR(16777216) UNIQUE,
	"FIRSTNAME" VARCHAR(16777216) NOT NULL,
	"LASTNAME" VARCHAR(16777216) NOT NULL,
	"GENDER" VARCHAR(16777216) NOT NULL,
	"AGE" NUMBER(38,0) NOT NULL,
	"NATIONALITY" VARCHAR(16777216) NOT NULL,
	"TICKETTYPE" VARCHAR(16777216) NOT NULL,
	"PASSENGERSTATUS" VARCHAR(16777216) NOT NULL
);

CREATE TABLE IF NOT EXISTS AIRPORTS (
	"AIRPORTID" INT AUTOINCREMENT start 1 increment 1 PRIMARY KEY,
	"ARRIVALAIRPORT" VARCHAR(16777216)  NOT NULL,
	"AIRPORTNAME" VARCHAR(16777216) UNIQUE
);

CREATE TABLE IF NOT EXISTS COUNTRIES (
	"COUNTRYID" INT AUTOINCREMENT start 1 increment 1 PRIMARY KEY,
	"AIRPORTCOUNTRYCODE" VARCHAR(16777216) UNIQUE,
	"COUNTRYNAME" VARCHAR(16777216) NOT NULL
);

CREATE TABLE IF NOT EXISTS CONTINENTS (
	"CONTINENTID" INT AUTOINCREMENT start 1 increment 1 PRIMARY KEY,
	"AIRPORTCONTINENT" VARCHAR(16777216) UNIQUE,
	"CONTINENTS" VARCHAR(16777216) NOT NULL
);

CREATE TABLE IF NOT EXISTS PLANES (
	"ID" NUMBER(38,0) AUTOINCREMENT start 1 increment 1 PRIMARY KEY,
	"PASSENGERID" INT,
	"AIRPORTID" INT,
	"COUNTRYID" INT,
	"CONTINENTID" INT,
	"DEPARTUREDATE" DATE NOT NULL,
	"PILOTNAME" VARCHAR(16777216) NOT NULL,
	"FLIGHTSTATUS" VARCHAR(16777216) NOT NULL,
	FOREIGN KEY ("PASSENGERID") REFERENCES PASSENGERS("PASSENGERID"),
	FOREIGN KEY ("AIRPORTID") REFERENCES AIRPORTS ("AIRPORTID"),
	FOREIGN KEY ("COUNTRYID") REFERENCES COUNTRIES ("COUNTRYID"),
	FOREIGN KEY ("CONTINENTID") REFERENCES CONTINENTS ("CONTINENTID")
);

CREATE TABLE IF NOT EXISTS ROLES_MAPPING(
    ARRIVALAIRPORT varchar PRIMARY KEY,
    ROLE_NAME varchar
);